export const normalizeShieldProgressionConfig = (shieldProgression?: ShieldProgressionConfig): NormalizedShieldProgressionConfig => {
  const defaultMode = filterDefaults.shieldProgression.mode
  const earlyMaxAreaLevel = filterDefaults.shieldProgression.earlyMaxAreaLevel
  const mode =
    typeof shieldProgression === "string" || shieldProgression === undefined
      ? (shieldProgression ?? defaultMode)
      : (shieldProgression.mode ?? defaultMode)
  const maxAreaLevel = typeof shieldProgression === "object" ? shieldProgression.maxAreaLevel : undefined
  if (mode === "none") return { enabled: false, maxAreaLevel: maxAreaLevel ?? earlyMaxAreaLevel }
  if (mode === "full") return { enabled: true, maxAreaLevel }
  return { enabled: true, maxAreaLevel: maxAreaLevel ?? earlyMaxAreaLevel }
}
