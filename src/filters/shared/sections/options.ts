export const SOCKET_COLOR_PRIORITY = { R: 0, G: 1, B: 2 } as const
export const SOCKET_EFFECT_COLOR_MAP = { R: "Red", G: "Green", B: "Blue" } as const satisfies Record<SocketColor, Color>
export const DEFENCE_TYPE_HIGHLIGHT_COLOR_MAP = {
  "armour": "Red",
  "evasion": "Green",
  "es": "Blue",
  "armour-evasion": "White",
  "armour-es": "White",
  "es-evasion": "White",
} as const satisfies Record<DefenceBaseType, Color>

export const normalizeSocketPattern = <TPattern extends SocketPattern>(pattern: string): TPattern => {
  const socketColors = [...pattern]
  if (!socketColors.every((color): color is SocketColor => color in SOCKET_COLOR_PRIORITY)) {
    throw new Error(`Invalid socket pattern "${pattern}". Only R, G, and B are allowed.`)
  }
  const colorCounts = socketColors.reduce<Record<SocketColor, number>>(
    (counts, color) => {
      counts[color] += 1
      return counts
    },
    { R: 0, G: 0, B: 0 },
  )
  return [...socketColors]
    .sort((left, right) => {
      const delta = colorCounts[right] - colorCounts[left]
      return delta !== 0 ? delta : SOCKET_COLOR_PRIORITY[left] - SOCKET_COLOR_PRIORITY[right]
    })
    .join("") as TPattern
}

export const getSocketPatternEffectColor = (pattern: string): Color => {
  const socketColors = [...pattern]
  if (!socketColors.every((color): color is SocketColor => color in SOCKET_COLOR_PRIORITY)) {
    throw new Error(`Invalid socket pattern "${pattern}". Only R, G, and B are allowed.`)
  }
  const colorCounts = socketColors.reduce<Record<SocketColor, number>>(
    (counts, color) => {
      counts[color] += 1
      return counts
    },
    { R: 0, G: 0, B: 0 },
  )
  const highest = Math.max(...Object.values(colorCounts))
  const highestColors = (Object.entries(colorCounts) as [SocketColor, number][])
    .filter(([, count]) => count === highest)
    .map(([color]) => color)
  return highestColors.length === 1 ? SOCKET_EFFECT_COLOR_MAP[highestColors[0]] : "White"
}

export const getDefenceTypeHighlightColor = (defenceType: DefenceBaseType): Color => DEFENCE_TYPE_HIGHLIGHT_COLOR_MAP[defenceType]

export const normalizeSocketPatternConfig = <TNormalized extends SocketPattern, TRaw extends string = string>(
  entry: TRaw | SocketPatternConfig<TRaw>,
): SocketPatternConfig<TNormalized> => {
  const normalized = typeof entry === "string" ? { pattern: entry } : entry
  return { ...normalized, pattern: normalizeSocketPattern<TNormalized>(normalized.pattern) }
}

